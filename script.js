// 5. Fonctions de gestion des cookies
function getCookie(name) {
  try {
    const value = `; ${document.cookie}`;
    const parts = value.split(`; ${name}=`);
    if (parts.length === 2) {
      const cookieValue = parts.pop().split(';').shift();
      console.log(`‚úì Cookie "${name}" trouv√©:`, cookieValue);
      return cookieValue;
    }
    console.log(`‚Ñπ Cookie "${name}" non trouv√©`);
    return null;
  } catch (error) {
    console.error(`‚úó Erreur lecture cookie "${name}":`, error);
    return null;
  }
}

function setCookie(name, value, days = 365) {
  try {
    const date = new Date();
    date.setTime(date.getTime() + (days * 24 * 60 * 60 * 1000));
    const expires = `expires=${date.toUTCString()}`;
    const secure = window.location.protocol === 'https:' ? ';Secure' : '';
    document.cookie = `${name}=${value};${expires};path=/;SameSite=Lax${secure}`;
    
    const verification = getCookie(name);
    if (verification === value.toString()) {
      console.log(`‚úì Cookie "${name}" cr√©√© avec succ√®s`);
      return true;
    } else {
      console.warn(`‚ö† Cookie "${name}" cr√©√© mais v√©rification √©chou√©e`);
      return false;
    }
  } catch (error) {
    console.error(`‚úó Erreur cr√©ation cookie "${name}":`, error);
    return false;
  }
}

