// 13. Attacher les listeners aux liens
function attachLinkTracking() {
  console.log('üîó Initialisation du tracking des liens...');
  console.log('üìç √âtat du DOM:', document.readyState);
  
  const links = document.querySelectorAll('a[href]');
  console.log(`‚Ñπ ${links.length} liens d√©tect√©s sur la page`);
  
  if (links.length === 0) {
    console.warn('‚ö† Aucun lien trouv√© ! Le DOM est peut-√™tre pas encore charg√©.');
    console.log('üìù Contenu du body:', document.body?.innerHTML?.substring(0, 200));
  }
  
  links.forEach((link, index) => {
    // Ignorer les liens d√©j√† track√©s
    if (link.dataset.tracked === 'true') {
      return;
    }
    
    // Marquer comme track√©
    link.dataset.tracked = 'true';
    
    // console.log(`  Lien ${index + 1}: ${link.href} - "${link.textContent.trim().substring(0, 30)}"`);
    
    link.addEventListener('click', function(event) {
      // Ignorer les liens avec href vide ou javascript:
      if (!this.href || this.href.startsWith('javascript:') || this.href.includes('#')) {
        console.log('üîó Lien ignor√© (ancre ou javascript):', this.href);
        return;
      }
      
      console.log('üñ±Ô∏è CLIC D√âTECT√â sur:', this.href);
      event.preventDefault();
      
      const targetUrl = this.href;
      
      addClickToVisit(this, event)
        .then(() => {
          console.log('‚Üí Navigation vers:', targetUrl);
          window.location.href = targetUrl;
        })
        .catch((error) => {
          console.error('‚úó Erreur lors du tracking, navigation quand m√™me:', error);
          window.location.href = targetUrl;
        });
    });
  });
  
  console.log('‚úì Tracking des liens activ√©');
}

// 13b. Observer les changements du DOM pour tracker les nouveaux liens
function observeDOMChanges() {
  console.log('üëÅÔ∏è Activation de l\'observateur DOM pour les liens dynamiques...');
  
  const observer = new MutationObserver((mutations) => {
    let hasNewLinks = false;
    
    mutations.forEach((mutation) => {
      mutation.addedNodes.forEach((node) => {
        // V√©rifier si le n≈ìud ajout√© contient des liens
        if (node.nodeType === 1) { // ELEMENT_NODE
          if (node.tagName === 'A' && node.href) {
            hasNewLinks = true;
          } else if (node.querySelectorAll) {
            const links = node.querySelectorAll('a[href]');
            if (links.length > 0) {
              hasNewLinks = true;
            }
          }
        }
      });
    });
    
    // Si de nouveaux liens ont √©t√© ajout√©s, les tracker
    if (hasNewLinks) {
      console.log('üîÑ Nouveaux liens d√©tect√©s, r√©attachement du tracking...');
      attachLinkTracking();
    }
  });
  
  // Observer tout le body pour les changements
  observer.observe(document.body, {
    childList: true,
    subtree: true
  });
  
  console.log('‚úì Observateur DOM activ√©');
}

